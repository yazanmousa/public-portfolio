package app.models.image;

import app.models.organisation.Organisation;
import app.models.user.User;

import javax.persistence.*;

/**
 * @author Yazan
 * Date 13/05/2021 20:57
 */
@Entity
public class ImageOrganisation {
    public ImageOrganisation() {
        super();
    }

    public ImageOrganisation(String name, String type, byte[] picByte) {
        this.name = name;
        this.type = type;
        this.picByte = picByte;
    }

    @Id
    @Column(name = "id")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(name = "name")
    private String name;

    @Column(name = "type")
    private String type;

    //image bytes can have large lengths so we specify a value
    //which is more than the default length for picByte column
    @Column(name = "picByte", length = 999999999)
    private byte[] picByte;

    private long organisationId;



    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public byte[] getPicByte() {
        return picByte;
    }

    public long getOrganisationId() {
        return organisationId;
    }

    public void setOrganisationId(long organisationId) {
        this.organisationId = organisationId;
    }

    public void setPicByte(byte[] picByte) {
        this.picByte = picByte;
    }


}
